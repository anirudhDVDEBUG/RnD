# ---------------------------------------------------------------------------
# Live research (requires OPENROUTER_API_KEY)
# ---------------------------------------------------------------------------

def _live_research(query: str, depth: str) -> str:
    """Call OpenRouter with Tongyi Qwen-2.5 30B for real research."""
    try:
        import httpx
    except ImportError:
        return "ERROR: httpx not installed. Run: pip install httpx"

    api_key = os.environ.get("OPENROUTER_API_KEY", "")
    if not api_key:
        return "ERROR: OPENROUTER_API_KEY not set."

    depth_tokens = {"quick": 1024, "standard": 2048, "deep": 4096}
    max_tokens = depth_tokens.get(depth, 2048)

    system_prompt = (
        "You are a deep research assistant. Given a query, provide a thorough, "
        "well-cited synthesis. Format output as markdown with inline citations "
        "linking to real URLs. Include an executive summary, key findings, "
        "detailed analysis, and a sources table."
    )

    resp = httpx.post(
        "https://openrouter.ai/api/v1/chat/completions",
        headers={
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
        },
        json={
            "model": "qwen/qwen-2.5-72b-instruct",
            "max_tokens": max_tokens,
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": f"Research this topic thoroughly: {query}"},
            ],
        },
        timeout=120,
    )
    resp.raise_for_status()
    data = resp.json()
    return data["choices"][0]["message"]["content"]


# ---------------------------------------------------------------------------
# Request handler
# ---------------------------------------------------------------------------

def handle(msg: dict) -> dict | None:
    method = msg.get("method", "")
    msg_id = msg.get("id")

    if method == "initialize":
        return {
            "jsonrpc": "2.0",
            "id": msg_id,
            "result": {
                "protocolVersion": "2024-11-05",
                "serverInfo": {"name": "gigaxity-deep-research", "version": "0.1.0"},
                "capabilities": {"tools": {}},
            },
        }

    if method == "notifications/initialized":
        return None  # no response needed

    if method == "tools/list":
        return {
            "jsonrpc": "2.0",
            "id": msg_id,
            "result": {"tools": TOOLS},
        }

    if method == "tools/call":
        tool_name = msg.get("params", {}).get("name", "")
        args = msg.get("params", {}).get("arguments", {})

        if tool_name == "deep_research":
            query = args.get("query", "unknown topic")
            depth = args.get("depth", "standard")

            use_mock = os.environ.get("MOCK", "0") == "1" or not os.environ.get("OPENROUTER_API_KEY")

            if use_mock:
                report = MOCK_REPORT.format(
                    query=query,
                    depth=depth,
                    timestamp=datetime.now(timezone.utc).isoformat(),
                )
            else:
                report = _live_research(query, depth)

            return {
                "jsonrpc": "2.0",
                "id": msg_id,
                "result": {
                    "content": [{"type": "text", "text": report}],
                },
            }

        return {
            "jsonrpc": "2.0",
            "id": msg_id,
            "error": {"code": -32601, "message": f"Unknown tool: {tool_name}"},
        }

    # Unknown method
    if msg_id is not None:
        return {
            "jsonrpc": "2.0",
            "id": msg_id,
            "error": {"code": -32601, "message": f"Unknown method: {method}"},
        }
    return None


# ---------------------------------------------------------------------------
# Main loop
# ---------------------------------------------------------------------------

def main():
    while True:
        msg = _read()
        if msg is None:
            break
        resp = handle(msg)
        if resp is not None:
            _send(resp)


if __name__ == "__main__":
    main()
