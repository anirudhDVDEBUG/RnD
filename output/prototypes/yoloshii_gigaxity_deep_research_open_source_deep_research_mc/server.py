#!/usr/bin/env python3
"""
gigaxity-deep-research MCP server — local demo / mock mode.

Real mode: sends queries to OpenRouter (Tongyi Qwen 30B) + web search,
returns a cited synthesis.

Mock mode (MOCK=1 or no OPENROUTER_API_KEY): returns a realistic
canned response so you can verify the MCP wiring without burning tokens.
"""
import json
import os
import sys
import textwrap
from datetime import datetime, timezone

# ---------------------------------------------------------------------------
# MCP protocol helpers (stdio JSON-RPC)
# ---------------------------------------------------------------------------

def _send(obj: dict) -> None:
    """Write a JSON-RPC message to stdout."""
    payload = json.dumps(obj)
    sys.stdout.write(f"Content-Length: {len(payload)}\r\n\r\n{payload}")
    sys.stdout.flush()


def _read() -> dict | None:
    """Read one JSON-RPC message from stdin."""
    # Read headers
    headers: dict[str, str] = {}
    while True:
        line = sys.stdin.readline()
        if not line or line == "\r\n" or line == "\n":
            break
        if ":" in line:
            k, v = line.split(":", 1)
            headers[k.strip()] = v.strip()
    length = int(headers.get("Content-Length", 0))
    if length == 0:
        return None
    body = sys.stdin.read(length)
    return json.loads(body)

# ---------------------------------------------------------------------------
# Tool definitions
# ---------------------------------------------------------------------------

